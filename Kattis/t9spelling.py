def p(x):
    print(x, end='')
for t_c in range(1, T+1):
    inp = input()
    p('Case #{}: '.format(t_c))
    p(ph[inp[0]][1])
    prev = inp[0]
    for i in range(1, len(inp)):
        if ph[inp[i]][0] == ph[prev][0]:
            p(' ')
            p(ph[inp[i]][1])
        else:
            p(ph[inp[i]][1])
        prev = inp[i]
    print()

